package entities;

public class Departamento {

	private String name;
	
	public Departamento() {		
	}

	public Departamento(String departamento) {
		this.name = departamento;
	}

	public String getNome() {
		return name;
	}

	public void setDepartamento(String departamento) {
		this.name = departamento;
	}
	
	
	
}
